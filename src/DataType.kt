import java.util.*

class DataType {

    val a : Int =100
    val d : Double = 10.89
    val f : Float = 122.78f
    val ch : Char = 'B'

    // type conversion type casting int to double etc is done by explicit way

    //Operator
}

fun main(args: Array<String>) {

    val ob1 = DataType()
    println("The value of integer is ${ob1.a}")
    println("The value of double is ${ob1.d}")
    println("The value of float is ${ob1.f}")
    println("The value of char is ${ob1.ch}")


}